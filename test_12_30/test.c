/*
全局变量、局部变量、静态局部变量、静态全局变量的区别

1.全局变量
    ―― 在{}外部定义的变量，全局变量具有外部链接属性，在定义的全局变量中，
    任意一个源文件均可引用此全局变量

2.局部变量
    ―― 在{}内部定义的变量，局部变量只在函数执行期间存在，函数的调用结束后，
    局部变量所占的内存被收回

3.静态全局变量
    ―― 在{}外部使用static关键字修饰的变量，使用static修饰后，
    全局变量的外部链接属性变为内部链接属性，只能在本文件内可以访问

4.静态局部变量
    ―― 在{}内部使用static关键字修饰的变量，使用static修饰后，静态局部变量只会被初始化一次，
    它随着程序的结束而结束，局部变量的声明周期变长，静态局部变量只对定义自己的函数体始终可见

5.全局变量、静态全局变量、静态局部变量都在静态存储区中分配内存，局部变量在栈中分配内存空间
*/

//全局变量：在{}外定义的变量，在静态区中分配内存空间
int data = 20;//全局变量

//静态全局变量：使全局变量的外部链接属性变成内部链接属性，使只能在本文件内使用
static int n = 40;//全局变量

int main()
{
    //局部变量：在{}内定义，栈中分配内存空间
    int num = 10;//num是局部变量

    //静态局部变量：只初始化一次，局部变量的生命周期变长，静态局部变量只对自己所在的{}可见
    static int age = 18;//静态局部变量
	return 0;
}