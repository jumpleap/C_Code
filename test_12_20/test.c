#define _CRT_SECURE_NO_WARNINGS
#include <stdio.h>
#include <math.h>

/*
描述
	春天是鲜花的季节，水仙花就是其中最迷人的代表，数学上有个水仙花数，
	他是这样定义的： “水仙花数”是指一个三位数，它的各位数字的立方和等于其本身，
	比如：153=1^3+5^3+3^3。 现在要求输出所有在m和n范围内的水仙花数。
输入描述：
	输入数据有多组，每组占一行，包括两个整数m和n（100 ≤ m ≤ n ≤ 999）。
输出描述：
	对于每个测试实例，要求输出所有在给定范围内的水仙花数，就是说，
	输出的水仙花数必须大于等于m,并且小于等于n，如果有多个，
	则要求从小到大排列在一行内输出，之间用一个空格隔开; 如果给定的范围内不存在水仙花数，则输出no; 
	每个测试实例的输出占一行。
*/

int main()
{
    int m, n;
    int temp = 0;
    while (scanf("%d %d", &m, &n) != EOF)
    {
        temp = 0;//标记是否有水仙花数
        //进行判断数的范围
        if (m < 100 || n > 999)
            continue;
        for (; m <= n; m++)
        {
            int x = m % 10;//个位
            int y = m / 10 % 10;//十位
            int z = m / 100;//百位

            //判断是否相等
            if (pow(x, 3) + pow(y, 3) + pow(z, 3) == m)
            {
                temp = 1;//有水仙花数则标志为1
                printf("%d ", m);
            }
        }

        //没有水仙花数
        if (temp == 0)
            printf("no\n");
    }
    return 0;
}